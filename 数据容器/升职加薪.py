# 并通过for循环，对所有级别为1级的员工，级别上升1级，薪水增加1000元





my_dict = dict({'王力鸿':{'部门':'科技部','工资':3000,"级别":1},'林俊节': {'部门': "市场部",'工资':6000,'级别': 3},
                '周杰轮': {'部门':'市场部','工资':5000,'级别':2},'张学油': {'部门':'科技部','工资': 4000,'级别':1},
                '刘德滑': {'部门': '市场部' ,"工资":6000,'级别': 23}})

# 升职加薪
my_keys = my_dict.keys()
for i in my_keys:
    if my_dict[i]['级别'] == 1:
        my_dict[i]['工资'] += 1000
        my_dict[i]['级别'] += 1
    print(my_dict)